    | on:
    |   schedule:
    |     # Runs at 00:00 UTC every day
    |     - cron: '0 0 * * *'
    |
    | jobs:
    |
    |   update:
    |     runs-on: ubuntu-latest
    |     steps:
    |     - uses: actions/checkout@v2
    |
    |     - name: Install Deno
    |       uses: denolib/setup-deno@master
    |
    |     - name: Update dmm
    |       run: |
    |         deno run --allow-read --allow-write --allow-net https://deno.land/x/dmm/mod.ts update
    |         cd tests
    |         deno run --allow-read --allow-write --allow-net https://deno.land/x/dmm/mod.ts update
    |
    |     - name: Create pull request
    |       uses: peter-evans/create-pull-request@v3
    |       with:
    |         token: {{ token }}
    |         commit-message: 'Update dependencies'
    |         title: 'chore: Update dependencies'
    |         body: 'This pull request was auto-generated by GitHub Actions.'
    |         branch: update-dependencies
  p dmm will automatically run a sweep of checks on the repository's dependencies daily and create a pull request if updates are found.
